/* FIPS 197  P.21 Figure 12 */ //復号化
int invCipher(int data[])
{
  int i;

  AddRoundKey(data,nr);

  for(i=nr-1;i>0;i--)
  {
    invShiftRows(data);
    invSubBytes(data);
    AddRoundKey(data,i);
    invMixColumns(data,i);
  }

  invShiftRows(data);
  invSubBytes(data);
  AddRoundKey(data,0);
  return(nr);
}

