int main(int argc,char *argv[]){
  char *str; //ファイル名に関するポインタ
  int pad[argc-1]; //各々のファイルのパディング長を記憶する配列
  int flag; //暗号化処理をするか復号処理をするかを決めるフラグ
  int e_flag = 0; //エラーが発生する度にインクリメントする
  int i;
  int fd;

  time_t t1,t2;

  /*コマンドライン引数が1以下の時エラー処理*/
  if(argc <= 1){
    printf("The number of arguments is different.\n");
    return -1;
  }

  while(1){
    printf("Please input interger to decide whether encrypt or decrypt files.\n");
    printf("If you input 0,decryption will start.\n");
    printf("If you input otherwise,encryption will start.\n");
    printf("input:");
    scanf("%d",&flag);
    
    t1 = time(NULL);

    for(i = 0;i < argc-1;i++){
      if(flag){ //暗号化処理
	printf("[%d]%-40s is encrypting...:",i+1-e_flag,argv[i+1]); fflush(stdout);
	pad[i] = AES(argv[i+1],128,pad[i],flag); //暗号化・復号	
      }else{ //復号処理
	str = MakeFileName(strlen(argv[i+1])+7);
	strcpy(str,argv[i+1]);
	strcat(str,":Encode");
	printf("[%d]%-40s is decrypting...:",i+1-e_flag,str); fflush(stdout);
	pad[i] = AES(str,128,pad[i],flag); //暗号化・復号
      }
    
      /*AES関数から返ってきた値が-1の時エラー処理*/
      if(pad[i] < 0){
	if(flag)
	  printf("The error has occured while encrypting the file,\"%s\"\n",argv[i+1]);
	else
	  printf("The error has occured while decrypting the file,\"%s\"\n",argv[i+1]);
	
	e_flag++;
	continue;
      }
      
      printf("done\n");
    }

    t2 = time(NULL);
    printf("time = %d[s]\n",(int)(t2-t1));
  
    printf("Do you continue encryption or decryption?\n");
    printf("Please input integer(0:No otherwise:Yes):");
    scanf("%d",&flag);
    
    if(!flag)
      break;

    e_flag = 0;
  }

  printf("End.\n");
  
  return 0;
}

/*
  file_name:暗号化・復号対象のファイル名
  k_len:鍵長(単位はbit)
  pad_len:file_nameのパディング長
  flag:0の時file_nameの中身を復号,0以外の時file_nameの中身を暗号化
*/
int AES(char *file_name,int k_len,int pad_len,int flag){
  unsigned char keys[]={0x07,0x16,0x11,0x0b,0x13,0x15,0x0a,0x02,
                        0x1a,0x02,0x14,0x18,0x16,0x0c,0x0d,0x12,
                        0x10,0x0d,0x1a,0x02,0x0a,0x09,0x07,0x18,
                        0x04,0x16,0x06,0x10,0x18,0x1a,0x1f,0x1d}; //32byte(=256bit)
  FILE *fp1 = fopen(file_name,"rb"); //読み込み用ファイルポインタ
  FILE *fp2; //ファイル書き込み用ポインタ
  FILE *fp3;
  unsigned char *ctext; //暗号後のファイルの中身or復号対象ファイルの中身
  unsigned char *p;
  unsigned char *ptext; //復号後のファイルの中身or暗号対象ファイルの中身
  char *p_file_name;
  char *c_file_name;
  unsigned char *pad; //パディングを格納する為のポインタ
  long int psize,csize; //ファイルサイズ
  int fd; //ftruncate関数を扱うための変数
  int tru; //削るバイト数
  unsigned char xor;
  long int i,j;
  
  /*ファイルがオープンできなかった時,エラー処理*/
  if(fp1 == NULL){
    printf("error\nThe file cannot open(read stream for %s is not available).\n",file_name);
    return -1;
  }

  /*鍵生成*/
  memcpy(key,keys,k_len/8);
  nk = k_len/32;               //鍵の長さ 4,6,8(128,192,256 bit)
  nr = nk + 6;          //ラウンド数 10,12,14
  KeyExpansion(key);

  srand((unsigned)time(NULL));
  
  if(flag){ /*-------------------------暗号化ブロック-------------------------*/
    psize = get_read_file_size(file_name); //暗号対象ファイルのサイズを取得
    pad_len = NBb-(psize%NBb); //パディング長(取りうる値は1?NBb)
    csize = 3*(psize+pad_len)-20*NBb; //暗号化後のファイルサイズ
    ptext = MemMaker(psize+pad_len); //(psize+pad_len)[Byte]分メモリを確保
    ctext = MemMaker(csize); //csize[Byte]分メモリを確保

    if(psize <= 10*NBb){
      printf("error\nThe size of %s is too short.\n",file_name);
      
      fclose(fp1);
      
      return -1;
    }

    fread(ptext,sizeof(unsigned char),psize,fp1); //暗号対象ファイルの中身をptextに格納
    p = MemMaker(psize);
    memcpy(p,ptext,psize);

    fclose(fp1);

    /*streamを作成*/
    c_file_name = MakeFileName(strlen(file_name)+7);
    strcpy(c_file_name,file_name);
    strcat(c_file_name,":Encode");

    /* 暗号化アルゴリズム(ただし先頭160Byteは暗号化しない) */
    for(i = 10*NBb;i < psize;i += NBb){
      if(i+NBb < psize){
	memcpy(data,ptext+i,NBb); //ファイルデータを16Byte(=128bit)毎のブロックに分けて暗号化
	Cipher(data); //暗号化
	memcpy(ptext+i,data,NBb); //暗号文を対応する部分に上書き
      }else{
	memcpy(data,ptext+i,psize-i); //text内の残りのデータをdataにコピー

	pad = MemMaker(pad_len); //パディング用のメモリをpad_len[Byte]分を確保する
	for(j = 0;j < pad_len;j++)
	  *(pad+j) = pad_len; //全てのメモリにpad_lenを格納
	
	if(pad_len < NBb)
	  memcpy((unsigned char *)data+(psize-i),pad,pad_len); //dataの末尾にパディングを付加
	Cipher(data); //暗号化
	memcpy(ptext+i,data,NBb); //暗号文を対応する部分に上書き
      }
    }

    /*暗号ファイルのバイナリデータを作成*/
    memcpy(ctext,ptext,10*NBb); //暗号化されていない最初の160Byteをまずコピーする
    for(i = 0;i < psize+pad_len-10*NBb;i += NBb){
      for(j = 3*i+10*NBb;j < 3*i+12*NBb;j++)
	*(ctext+j) = 0;
      memcpy(ctext+3*i+12*NBb,ptext+i+10*NBb,NBb);
    }

    /*擬似乱数偶数と平文をxor(1Byte中0〜2bitを変換)*/
    for(i = 10*NBb;i < psize;i++){
      while(1){
	for(j = 0;j < NB;j++)
	  data[j] = rand();
	
	Cipher(data);

	xor = (unsigned char)data[0];
	if(xor == 0 || xor == 1 || xor == 2 || xor == 4 || xor == 8 || xor == 16 || xor == 32 || xor == 64 || xor == 128 ||
	   xor == 192 || xor == 160 || xor == 144 || xor == 136 || xor == 132 || xor == 130 || xor == 129 || xor == 96 || xor == 80 ||
	   xor == 72 || xor == 68 || xor == 66 || xor == 65 || xor == 48 || xor == 40 || xor == 36 || xor == 34 || xor == 33 ||
	   xor == 24 || xor == 20 || xor == 18 || xor == 17 || xor == 12 || xor == 10 || xor == 9 || xor == 6 || xor == 5 ||
	   xor == 3){
	  *(p+i) ^= xor;
	  break;
	}else
	  continue;
      }
    }

    /*暗号ファイルを少しずつ書き出す*/
    if(LOOP <= csize)
      tru = csize/LOOP;
    else
      tru = 1;
    for(i = 10*NBb;i < csize;i += tru){
      fp2 = fopen(file_name,"rb+"); //追記用にオープン
      fp3 = fopen(c_file_name,"ab"); //streamオープン
      
      /*ファイルをオープンできなかった時エラー処理*/
      if(fp2 == NULL){
	printf("error\nThe file cannot be opened(add stream for %s is not available).\n",file_name);

	if(fp3 != NULL)
	  fclose(fp3);
	free(ctext);
	free(p);
	free(pad);
	free(ptext);
	free(c_file_name);
	
	return -1;
      }
      if(fp3 == NULL){
	printf("error\nThe stream %s cannot be opened.\n",c_file_name);

	fclose(fp2);
	free(ctext);
	free(p);
	free(pad);
	free(ptext);
	free(c_file_name);

	return -1;
      }

      if(i == 10*NBb)
	fwrite(ctext,sizeof(unsigned char),10*NBb,fp3);

      fseek(fp2,i,SEEK_SET);

      if(i+tru < psize)
	fwrite(p+i,sizeof(unsigned char),tru,fp2); //tru[Byte]ずつ書き出す
      else if(i < psize)
	fwrite(p+i,sizeof(unsigned char),psize-i,fp2);

      if(i+tru < csize)
	fwrite(ctext+i,sizeof(unsigned char),tru,fp3);
      else if(i < csize)
	fwrite(ctext+i,sizeof(unsigned char),csize-i,fp3); 
      
      fclose(fp2);
      fclose(fp3);
    }

    free(pad);
    free(p);
    free(ctext);
    free(ptext);
    free(c_file_name);
  }else{ /*-------------------------復号ブロック-------------------------*/
    csize = get_read_file_size(file_name); //復号対象ファイルのサイズを取得
    psize = (csize+20*NBb)/3; //復号後のファイルのサイズを取得
    ctext = MemMaker(csize); //csize[Byte]分メモリを確保
    ptext = MemMaker(psize); //psize[Byte]分メモリを確保
    
    fread(ctext,sizeof(unsigned char),csize,fp1); //復号対象ファイルの中身をctextに格納

    fclose(fp1);

    /*復号後のファイル名*/
    p_file_name = MakeFileName(strlen(file_name)-7);
    for(i = 0;i < strlen(file_name)-7;i++)
      *(p_file_name+i) = *(file_name+i);
    *(p_file_name+i) = '\0';
    
    /*暗号ファイルのバイナリから,復号に必要なデータだけを抽出する*/
    memcpy(ptext,ctext,10*NBb); //先頭160Byteをまずコピーする
    for(i = 0;i <  psize-10*NBb;i += NBb)
      memcpy(ptext+i+10*NBb,ctext+3*i+12*NBb,NBb);

    /*復号アルゴリズム(ただし先頭160Byteは復号しない)*/
    for(i = 10*NBb;i < psize;i += NBb){
      memcpy(data,ptext+i,NBb); //ファイルデータを16Byte(=128bit)毎のブロックに分けて復号
      invCipher(data); //復号
      if(i+NBb < psize){
	memcpy(ptext+i,data,NBb); //復号データを対応する箇所にコピー
      }else{
	psize -= pad_len; //パディングの除去
	memcpy(ptext+i,data,psize-i); //パディングを除去したデータをコピー
      }
    }

    fp2 = fopen(p_file_name,"rb+"); //追記用にオープン
    
    /*オープンできなかった時エラー処理*/
    if(fp2 == NULL){
      printf("error\nThe file cannot be opened(add stream for %s is not available).\n",p_file_name);

      free(ctext);
      free(ptext);
      
      return -1;
    }
    
    fseek(fp2,0,SEEK_SET);
    fwrite(ptext,sizeof(unsigned char),psize,fp2);
    
    fclose(fp2); //file_nameをクローズ
    remove(file_name);
    free(ptext);
    free(ctext);
  }
    
  return flag ? pad_len : 0; //暗号化の時はパディング長を返し,復号の時は0を返す
}

/*nバイト分メモリを確保する*/
unsigned char *MemMaker(long int n){
  return (unsigned char *)malloc(sizeof(unsigned char) * n);
}

/*(n+1)バイト分メモリを確保*/
char *MakeFileName(long int n){
  return (char *)malloc(sizeof(char) * (n+1));
}
 
