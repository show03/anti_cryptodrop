/* FIPS 197  P.15 Figure 5 */ //暗号化
int Cipher(int data[])
{
  int i;

  AddRoundKey(data,0);

  for(i=1;i<nr;i++)
  {
    SubBytes(data);
    ShiftRows(data);
    MixColumns(data);
    AddRoundKey(data,i);
  }

  SubBytes(data);
  ShiftRows(data);
  AddRoundKey(data,i);
  return(i);
}

